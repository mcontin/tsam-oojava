
public class Vars {
	
	public static void main(String[] args){
		System.out.println("Vars :)\n");
		
		//dichiarazione variabili:
		
		//int
		int i1 = 10;
		int i2 = 15;
		
		System.out.println("** int **");	
		System.out.println(i1+i2+"\n");

		//double
		double d1 = 10;
		double d2 = 15.5;
		
		System.out.println("** double **");	
		System.out.println(d1+d2+"\n");

		//boolean
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println("** boolean **");	
		System.out.println((b1||b2)+"\n");
		
		//char
		char c1 = 'd';
		char c2 = 'c';
		
		System.out.println("** boolean **");	
		System.out.println(c1+" "+c2+"\n");

		//byte 
		byte byte1 = 0;
		byte byte2 = 127;
		//byte byte3 = 128;
		
		//long 
		long long1 = 0;
		long long2 = 30000000000000L;
		
		//float
		float f1 = 23;
		float f2 = 12.2F;
		
		//string
		String str1 = "";
		String str2 = "Hello";
		String str3 = null;
	}
}
